package module1._3expressions;

public class E05OperatorExample {
	
	//Exercise 5: Do these operations work as you expect? What
	//is happening with division?
	public static void main(String[] args) {
		System.out.println(2 + 3);
		System.out.println(2 - 3);
		System.out.println(2 * 3);
		System.out.println(2 / 3);
		System.out.println(2 == 3);
		System.out.println(2 != 3);
	}
}
