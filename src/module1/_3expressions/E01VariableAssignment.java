package module1._3expressions;

public class E01VariableAssignment {
	//Exercise 1: Run this code and pay attention to how
	//the values of the variables are changing. Make sure
	//you understand what is happening before moving on.
	public static void main(String[] args) {
		int x = 3;
		int y = 2;
		System.out.println(x);
		System.out.println(y);
		x = y;
		System.out.println(x);
		System.out.println(y);
		y = 5;
		System.out.println(x);
		System.out.println(y);
	}
}
