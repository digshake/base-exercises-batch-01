package module1._6boolean;

public class E02RelationalOperators {
	
	//Exercise 2: Predict the output of the code before you run it
	public static void main(String[] args) {
		int age = 15;
		int year = 14;
		// Will these print true or false?
		System.out.println( age < year );
		System.out.println( age > year );
		System.out.println( age <= year+1 );
		System.out.println( age-1 >= year );
	}
}
